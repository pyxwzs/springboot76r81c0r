<script>
	import { 
		isMobile,
	} from "@/utils/validate";
	export default {
		data() {
			var validateMobile = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isMobile(value)) {
					callback(new Error("请输入正确的手机号码"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					yinhuanbianhao : false,
					yinhuanmingcheng : false,
					yinhuanleixing : false,
					yinhuanweizhi : false,
					yinhuandengji : false,
					yinhuanneirong : false,
					shangbaoriqi : false,
					yinhuanzhuangtai : false,
					zhanghao : false,
					xingming : false,
					shoujihaoma : false,
				},
			
				ruleForm: {
					yinhuanbianhao: this.getUUID(),
					yinhuanmingcheng: '',
					yinhuanleixing: '',
					yinhuanweizhi: '',
					yinhuandengji: '四级',
					yinhuanneirong: '',
					shangbaoriqi: '',
					yinhuanzhuangtai: '',
					zhanghao: '',
					xingming: '',
					shoujihaoma: '',
				},
				yinhuanzhuangtaiOptions: [],

				rules: {
					yinhuanbianhao: [
					],
					yinhuanmingcheng: [
					],
					yinhuanleixing: [
					],
					yinhuanweizhi: [
					],
					yinhuandengji: [
					],
					yinhuanneirong: [
					],
					shangbaoriqi: [
					],
					yinhuanzhuangtai: [
					],
					zhanghao: [
					],
					xingming: [
					],
					shoujihaoma: [
						{ validator: validateMobile, trigger: 'blur' },
					],
				},
			};
		},
		props: ["parent"],
		computed: {
			sessionForm() {
				return JSON.parse(this.$storage.getObj('userForm'))
			},
			sessionTable() {
				return this.$storage.get('sessionTable')
			},



		},
		components: {
		},
		created() {
			this.ruleForm.shangbaoriqi = this.getCurDate()
		},
		methods: {
			imgPreView(url){
				this.$parent.imgPreView(url)
			},
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type ) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='yinhuanbianhao'){
							this.ruleForm.yinhuanbianhao = obj[o];
							this.ro.yinhuanbianhao = true;
							continue;
						}
						if(o=='yinhuanmingcheng'){
							this.ruleForm.yinhuanmingcheng = obj[o];
							this.ro.yinhuanmingcheng = true;
							continue;
						}
						if(o=='yinhuanleixing'){
							this.ruleForm.yinhuanleixing = obj[o];
							this.ro.yinhuanleixing = true;
							continue;
						}
						if(o=='yinhuanweizhi'){
							this.ruleForm.yinhuanweizhi = obj[o];
							this.ro.yinhuanweizhi = true;
							continue;
						}
						if(o=='yinhuandengji'){
							this.ruleForm.yinhuandengji = obj[o];
							this.ro.yinhuandengji = true;
							continue;
						}
						if(o=='yinhuanneirong'){
							this.ruleForm.yinhuanneirong = obj[o];
							this.ro.yinhuanneirong = true;
							continue;
						}
						if(o=='shangbaoriqi'){
							this.ruleForm.shangbaoriqi = obj[o];
							this.ro.shangbaoriqi = true;
							continue;
						}
						if(o=='yinhuanzhuangtai'){
							this.ruleForm.yinhuanzhuangtai = obj[o];
							this.ro.yinhuanzhuangtai = true;
							continue;
						}
						if(o=='zhanghao'){
							this.ruleForm.zhanghao = obj[o];
							this.ro.zhanghao = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
						if(o=='shoujihaoma'){
							this.ruleForm.shoujihaoma = obj[o];
							this.ro.shoujihaoma = true;
							continue;
						}
					}
					this.ruleForm.yinhuandengji = '四级'; 				}
				// 获取用户信息
				this.$http({
					url: `${this.sessionTable}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
						if(((json.zhanghao!=''&&json.zhanghao) || json.zhanghao==0) && this.sessionTable!="users"){
							this.ruleForm.zhanghao = json.zhanghao
							this.ro.zhanghao = true;
						}
						if(((json.xingming!=''&&json.xingming) || json.xingming==0) && this.sessionTable!="users"){
							this.ruleForm.xingming = json.xingming
							this.ro.xingming = true;
						}
						if(((json.shoujihaoma!=''&&json.shoujihaoma) || json.shoujihaoma==0) && this.sessionTable!="users"){
							this.ruleForm.shoujihaoma = json.shoujihaoma
							this.ro.shoujihaoma = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
				this.yinhuanzhuangtaiOptions = "已处理,待处理".split(',')
			
			},
			// 多级联动参数

			async info(id) {
				await this.$http({
					url: `yinhuanshangbao/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
						this.ruleForm.yinhuanneirong = this.ruleForm.yinhuanneirong.replace(reg,'../../../springboot76r81c0r/upload');
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.yinhuanbianhao) {
						this.ruleForm.yinhuanbianhao = String(this.ruleForm.yinhuanbianhao)
					}
					var objcross = this.$storage.getObj('crossObj');
					if(!this.ruleForm.id) {
						delete this.ruleForm.userid
					}
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							await this.$http({
								url: `yinhuanshangbao/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.yinhuanshangbaoCrossAddOrUpdateFlag = false;
											this.parent.search();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.yinhuanshangbaoCrossAddOrUpdateFlag = false;
			},
		}
	};
</script>
